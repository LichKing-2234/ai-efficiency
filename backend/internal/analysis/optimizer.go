// Optimizer creates auto-fix PRs based on scan results.
type Optimizer struct {
	llmAnalyzer *llm.Analyzer
	logger      *zap.Logger
}

// NewOptimizer creates a new Optimizer.
func NewOptimizer(llmAnalyzer *llm.Analyzer, logger *zap.Logger) *Optimizer {
	return &Optimizer{
		llmAnalyzer: llmAnalyzer,
		logger:      logger,
	}
}

// OptimizeResult holds the result of an optimization attempt.
type OptimizeResult struct {
	BranchName string `json:"branch_name"`
	PRURL      string `json:"pr_url,omitempty"`
	PRID       int    `json:"pr_id,omitempty"`
	FilesAdded int    `json:"files_added"`
}

// OptimizePreviewFile holds old/new content for a single file preview.
type OptimizePreviewFile struct {
	Path       string `json:"path"`
	OldContent string `json:"old_content"`
	NewContent string `json:"new_content"`
	IsNew      bool   `json:"is_new"`
}

// OptimizePreview holds the full preview before PR creation.
type OptimizePreview struct {
	Files []OptimizePreviewFile `json:"files"`
	Score int                   `json:"score"`
}

// PreviewOptimization generates the optimization files and returns old/new content for review.
func (o *Optimizer) PreviewOptimization(
	ctx context.Context,
	provider scm.SCMProvider,
	rc *ent.RepoConfig,
	scanResult *ent.AiScanResult,
) (*OptimizePreview, error) {
	suggestions := extractSuggestions(scanResult)
	dimensions := extractDimensions(scanResult)

	files := make(map[string]string)
	for _, s := range suggestions {
		if !s.AutoFix {
			continue
		}
		switch {
		case s.Category == "ai_files" && strings.Contains(s.Message, "AGENTS.md"):
			content, err := o.generateAgentsMD(ctx, rc, dimensions, scanResult.Score)
			if err != nil {
				o.logger.Warn("failed to generate AGENTS.md via LLM, using template", zap.Error(err))
				content = generateAgentsMDTemplate(rc.FullName, dimensions)
			}
			files["AGENTS.md"] = content
		case s.Category == "ai_files" && strings.Contains(s.Message, ".editorconfig"):
			files[".editorconfig"] = generateEditorConfig()
		}
	}

	if len(files) == 0 {
		return nil, nil
	}

	var previewFiles []OptimizePreviewFile
	for path, newContent := range files {
		pf := OptimizePreviewFile{
			Path:       path,
			NewContent: newContent,
			IsNew:      true,
		}
		existing, err := provider.GetFileContent(ctx, rc.FullName, path, rc.DefaultBranch)
		if err == nil {
			old := string(existing)
			if strings.TrimSpace(old) == strings.TrimSpace(newContent) {
				continue // identical, skip
			}
			pf.OldContent = old
			pf.IsNew = false
		}
		previewFiles = append(previewFiles, pf)
	}

	if len(previewFiles) == 0 {
		return nil, nil
	}

	return &OptimizePreview{
		Files: previewFiles,
		Score: scanResult.Score,
	}, nil
}

// ConfirmOptimization takes the (possibly edited) files and creates the branch + PR.
func (o *Optimizer) ConfirmOptimization(
	ctx context.Context,
	provider scm.SCMProvider,
	rc *ent.RepoConfig,
	files map[string]string,
	score int,
) (*OptimizeResult, error) {
	if len(files) == 0 {
		return nil, fmt.Errorf("no files to commit")
	}

	branchName := fmt.Sprintf("ai-efficiency/optimize-%d", time.Now().Unix())
	baseSHA, err := provider.GetBranchSHA(ctx, rc.FullName, rc.DefaultBranch)
	if err != nil {
		return nil, fmt.Errorf("get branch SHA: %w", err)
	}
	if err := provider.CreateBranch(ctx, rc.FullName, branchName, baseSHA); err != nil {
		return nil, fmt.Errorf("create branch: %w", err)
	}

	fileList := make([]string, 0, len(files))
	for path := range files {
		fileList = append(fileList, path)
	}
	commitMsg := fmt.Sprintf("chore: add AI-friendly config files (%s)\n\nAuto-generated by AI Efficiency Platform based on scan score %d/100.",
		strings.Join(fileList, ", "), score)

	_, err = provider.CommitFiles(ctx, scm.CommitFilesRequest{
		RepoFullName: rc.FullName,
		Branch:       branchName,
		Message:      commitMsg,
		Files:        files,
	})
	if err != nil {
		return nil, fmt.Errorf("commit files: %w", err)
	}

	dims := make([]rules.DimensionScore, 0)
	prBody := buildPRDescription(files, dims, score)
	pr, err := provider.CreatePR(ctx, scm.CreatePRRequest{
		RepoFullName: rc.FullName,
		Title:        fmt.Sprintf("chore: improve AI-friendliness (score: %d/100)", score),
		Body:         prBody,
		SourceBranch: branchName,
		TargetBranch: rc.DefaultBranch,
	})
	if err != nil {
		return nil, fmt.Errorf("create PR: %w", err)
	}

	o.logger.Info("optimization PR created",
		zap.String("repo", rc.FullName),
		zap.String("branch", branchName),
		zap.Int("pr_id", pr.ID),
		zap.Int("files", len(files)),
	)

	return &OptimizeResult{
		BranchName: branchName,
		PRURL:      pr.URL,
		PRID:       pr.ID,
		FilesAdded: len(files),
	}, nil
}

// CreateOptimizationPR creates a PR with auto-fix files based on scan suggestions (legacy one-step flow).
func (o *Optimizer) CreateOptimizationPR(
	ctx context.Context,
	provider scm.SCMProvider,
	rc *ent.RepoConfig,
	scanResult *ent.AiScanResult,
) (*OptimizeResult, error) {
	preview, err := o.PreviewOptimization(ctx, provider, rc, scanResult)
	if err != nil {
		return nil, err
	}
	if preview == nil {
		return nil, nil
	}

	files := make(map[string]string)
	for _, f := range preview.Files {
		files[f.Path] = f.NewContent
	}
	return o.ConfirmOptimization(ctx, provider, rc, files, scanResult.Score)
}

// generateAgentsMD uses LLM if available, otherwise falls back to template.
func (o *Optimizer) generateAgentsMD(ctx context.Context, rc *ent.RepoConfig, dims []rules.DimensionScore, score int) (string, error) {
	if o.llmAnalyzer == nil || !o.llmAnalyzer.Enabled() {
		return generateAgentsMDTemplate(rc.FullName, dims), nil
	}

	var dimSummary strings.Builder
	for _, d := range dims {
		dimSummary.WriteString(fmt.Sprintf("- %s: %.0f/%.0f — %s\n", d.Name, d.Score, d.MaxScore, d.Details))
	}

	prompt := fmt.Sprintf(`Generate an AGENTS.md file for the repository "%s" (score: %d/100).

Scan dimension scores:
%s
This file helps AI coding assistants understand the project. Include:
- Project overview
- Tech stack and key dependencies
- Code structure and conventions
- Build and test commands
- Important patterns to follow

Keep it concise and actionable. Output ONLY the markdown content.`, rc.FullName, score, dimSummary.String())

	resp, err := o.llmAnalyzer.Chat(ctx, "You are a helpful assistant that generates developer documentation.", llm.ChatRequest{
		Message: prompt,
	})
	if err != nil {
		return "", err
	}
	return strings.TrimSpace(resp.Reply), nil
}

func generateAgentsMDTemplate(repoFullName string, dims []rules.DimensionScore) string {
	var sb strings.Builder
	sb.WriteString(fmt.Sprintf("# %s\n\n", repoFullName))
	sb.WriteString("## Project Overview\n\n")
	sb.WriteString("This file helps AI assistants understand the project structure and conventions.\n\n")
	sb.WriteString("## Code Style\n\n")
	sb.WriteString("- Follow existing code conventions in the repository\n")
	sb.WriteString("- Use consistent naming patterns\n\n")
	sb.WriteString("## Testing\n\n")
	sb.WriteString("- Run existing test suites before submitting changes\n")
	sb.WriteString("- Add tests for new functionality\n\n")
	sb.WriteString("## AI Scan Summary\n\n")
	for _, d := range dims {
		sb.WriteString(fmt.Sprintf("- %s: %.0f/%.0f — %s\n", d.Name, d.Score, d.MaxScore, d.Details))
	}
	return sb.String()
}

func generateEditorConfig() string {
	return `root = true

[*]
indent_style = space
indent_size = 2
end_of_line = lf
charset = utf-8
trim_trailing_whitespace = true
insert_final_newline = true

[*.go]
indent_style = tab

[*.md]
trim_trailing_whitespace = false

[Makefile]
indent_style = tab
`
}

func buildPRDescription(files map[string]string, dims []rules.DimensionScore, score int) string {
	var sb strings.Builder
	sb.WriteString("## AI Efficiency — Auto-Optimization\n\n")
	sb.WriteString(fmt.Sprintf("Current AI-friendliness score: **%d/100**\n\n", score))
	sb.WriteString("### Files Added/Updated\n\n")
	for path := range files {
		switch path {
		case "AGENTS.md":
			sb.WriteString("- **AGENTS.md** — Helps AI coding assistants understand the project\n")
		case ".editorconfig":
			sb.WriteString("- **.editorconfig** — Ensures consistent coding style across editors\n")
		default:
			sb.WriteString(fmt.Sprintf("- **%s**\n", path))
		}
	}
	sb.WriteString("\n### Current Scores\n\n")
	sb.WriteString("| Dimension | Score |\n|---|---|\n")
	for _, d := range dims {
		sb.WriteString(fmt.Sprintf("| %s | %.0f / %.0f |\n", d.Name, d.Score, d.MaxScore))
	}
	sb.WriteString("\n---\n*Generated by AI Efficiency Platform*\n")
	return sb.String()
}

// extractSuggestions converts scan result's JSON suggestions to typed structs.
func extractSuggestions(scan *ent.AiScanResult) []rules.Suggestion {
	if scan.Suggestions == nil {
		return nil
	}
	var result []rules.Suggestion
	for _, sMap := range scan.Suggestions {
		sg := rules.Suggestion{
			Category: stringVal(sMap, "category"),
			Message:  stringVal(sMap, "message"),
			Priority: stringVal(sMap, "priority"),
			AutoFix:  boolVal(sMap, "auto_fix"),
		}
		result = append(result, sg)
	}
	return result
}

// extractDimensions converts scan result's JSON dimensions to typed structs.
func extractDimensions(scan *ent.AiScanResult) []rules.DimensionScore {
	if scan.Dimensions == nil {
		return nil
	}
	var result []rules.DimensionScore
	for name, v := range scan.Dimensions {
		dMap, ok := v.(map[string]interface{})
		if !ok {
			continue
		}
		result = append(result, rules.DimensionScore{
			Name:     name,
			Score:    floatVal(dMap, "score"),
			MaxScore: floatVal(dMap, "max_score"),
			Details:  stringVal(dMap, "details"),
		})
	}
	return result
}

func stringVal(m map[string]interface{}, key string) string {
	v, _ := m[key].(string)
	return v
}

func boolVal(m map[string]interface{}, key string) bool {
	v, _ := m[key].(bool)
	return v
}

func floatVal(m map[string]interface{}, key string) float64 {
	switch v := m[key].(type) {
	case float64:
		return v
	case int:
		return float64(v)
	default:
		return 0
	}
}
